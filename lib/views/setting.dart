class SettingApp extends StatefulWidget {
  const SettingApp({
    Key? key,
  }) : super(key: key);

  @override
  State<SettingApp> createState() => _SettingAppState();
}

class _SettingAppState extends State<SettingApp> {
  bool useCustomTheme = false;

  final platformsMap = <DevicePlatform, String>{
    DevicePlatform.device: 'Default',
    DevicePlatform.android: 'Android',
    DevicePlatform.iOS: 'iOS',
    DevicePlatform.web: 'Web',
    DevicePlatform.fuchsia: 'Fuchsia',
    DevicePlatform.linux: 'Linux',
    DevicePlatform.macOS: 'MacOS',
    DevicePlatform.windows: 'Windows',
  };
  DevicePlatform selectedPlatform = DevicePlatform.fuchsia;

  final Future<SharedPreferences> _prefs = SharedPreferences.getInstance();

  final GlobalKey<FormState> _formKey = GlobalKey<FormState>();
  final GlobalKey<FormState> _ipFormKey = GlobalKey<FormState>();
  final GlobalKey<FormState> _appTitleFormKey = GlobalKey<FormState>();

  final TextEditingController _textEditingController = TextEditingController();

  final TextEditingController _ipEditingController = TextEditingController();
  final TextEditingController _appTitleEditingController =
      TextEditingController();

  late Future<String?> ndid = Future.value(null);
  Future<String?> mac = Future.value(null);
  Future<String?> ip = Future.value(null);
  Future<String?> subnetMask = Future.value(null);
  Future<String?> gateway = Future.value(null);

  @override
  void setState(VoidCallback fn) {
    if (mounted) {
      super.setState(fn);
    }
  }

  void updateUi(DeviceDisplay deviceDisplay) {
    mac = Future.value(deviceDisplay.mac);
    ip = Future.value(deviceDisplay.localIp);
    subnetMask = Future.value(deviceDisplay.subnetMask);
    gateway = Future.value(deviceDisplay.gateway);
    setState(() {});
  }

  Future<void> readCache() async {
    final SharedPreferences prefs = await SharedPreferences.getInstance();

    String? mac = prefs.getString("mac");
    if (mac == null) {
      return showSnackBar("未设置连接");
    }

    String? deviceData = prefs.getString(mac);
    if (deviceData == null) {
      return showSnackBar("未设置连接");
    }

    // List<int> data = deviceData.split(',').map((e) => int.parse(e)).toList();
    // updateUi(data);
  }

  Future<void> readHoldings1() async {
    final SharedPreferences prefs = await SharedPreferences.getInstance();

    int? index = prefs.getInt("no");
    if (index == null) {
      return showSnackBar("未设置连接");
    }
    try {
      DeviceDisplay? deviceDisplay =
          await api.halReadDeviceSettings(index: index);
      if (deviceDisplay == null) {
        return showSnackBar("读取设备出错");
      }
      updateUi(deviceDisplay);
    } catch (err) {
      debugPrint(err.toString());
      showSnackBar(err.toString());
    }
  }

  @override
  void initState() {
    super.initState();
    WidgetsBinding.instance.addPostFrameCallback((_) async {
      if (mounted) {
        readCache();
        await readHoldings1().whenComplete(() {
          debugPrint("读取完成");
        });
      }
    });
  }

  @override
  void dispose() {
    super.dispose();
  }

  void closeAppUsingSystemPop() {
    SystemChannels.platform.invokeMethod('SystemNavigator.pop');
  }

  void closeAppUsingExit() {
    exit(0);
  }

  Future<void> settingIplDialog(BuildContext context) async {
    return await showDialog(
        context: context,
        builder: (dialogContext) {
          bool isChecked = false;
          return StatefulBuilder(builder: (context, mountedState) {
            return AlertDialog(
              content: Form(
                key: _ipFormKey,
                child: Column(
                  mainAxisSize: MainAxisSize.min,
                  children: [
                    TextFormField(
                      keyboardType: TextInputType.url,
                      maxLength: 15,
                      maxLines: 1,
                      controller: _ipEditingController,
                      validator: (value) {
                        return value!.isNotEmpty ? null : "请输入IP地址";
                      },
                      decoration: const InputDecoration(
                        hintText: "IP地址(如:192.169.1.100)",
                      ),
                    ),
                  ],
                ),
              ),
              title: const Text('修改IP地址'),
              actions: <Widget>[
                TextButton(
                  onPressed: () {
                    Navigator.of(context).pop();
                  },
                  child: const Text('取消'),
                ),
                TextButton(
                  onPressed: () async {
                    if (_ipFormKey.currentState?.validate() ?? false) {
                      String url = _ipEditingController.text;
                      debugPrint("URL: $url");
                      List<int> values = url
                          .trim()
                          .split(".")
                          .map((e) => int.parse(e))
                          .toList();
                      String req = await api.halGenerateSetHoldingsBulk(
                          unitId: 1,
                          reg: 2247,
                          values: Uint16List.fromList(values));
                      bool res = await setHoldings(req);

                      if (res) {
                        showSnackBar("修改成功");
                        await readHoldings1();
                      } else {
                        showSnackBar("修改失败，请重试");
                      }
                      Navigator.of(context).maybePop();
                    }
                  },
                  child: Text('修改'),
                )
              ],
            );
          });
        });
  }

  Future<void> settingAppTitle(BuildContext context) async {
    return await showDialog(
        context: context,
        builder: (dialogContext) {
          return StatefulBuilder(builder: (context, mountedState) {
            return AlertDialog(
              content: Form(
                key: _appTitleFormKey,
                child: Column(
                  mainAxisSize: MainAxisSize.min,
                  children: [
                    TextFormField(
                      keyboardType: TextInputType.text,
                      maxLength: 30,
                      maxLines: 2,
                      controller: _appTitleEditingController,
                      validator: (value) {
                        return value!.isNotEmpty ? null : "请输入公司名称";
                      },
                      decoration: const InputDecoration(
                        hintText: "如:深圳市XXX有限公司",
                      ),
                    ),
                  ],
                ),
              ),
              title: const Text('设置应用标题'),
              actions: <Widget>[
                TextButton(
                  onPressed: () {
                    Navigator.of(context).pop();
                  },
                  child: const Text('取消'),
                ),
                TextButton(
                  onPressed: () async {
                    if (_appTitleFormKey.currentState?.validate() ?? false) {
                      final SharedPreferences prefs =
                          await SharedPreferences.getInstance();

                      bool res = await prefs.setString(
                          "appTitle", _appTitleEditingController.text);
                      if (res) {
                        showSnackBar("修改成功");
                        await readHoldings1();
                      } else {
                        showSnackBar("修改失败，请重试");
                      }
                      Navigator.of(context).maybePop();
                    }
                  },
                  child: Text('修改'),
                )
              ],
            );
          });
        });
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      body: SettingsList(
        platform: selectedPlatform,
        sections: [
          SettingsSection(
            title: Text('连接信息'),
            tiles: <SettingsTile>[
              SettingsTile.navigation(
                leading: Icon(Icons.network_cell),
                title: Text('当前连接地址'),
                value: FutureBuilder(
                  future: _prefs.then((SharedPreferences prefs) {
                    return prefs.getString('mac');
                  }),
                  initialData: 'MAC=',
                  builder: (context, AsyncSnapshot<String?> snapshot) {
                    if (snapshot.hasData &&
                        snapshot.connectionState == ConnectionState.done) {
                      String id = snapshot.data ?? '';
                      return Text(id);
                    }

                    return Text('MAC=');
                  },
                ),
              ),
            ],
          ),
          SettingsSection(
            title: Text("设备属性"),
            tiles: [
              SettingsTile.navigation(
                title: Text('MAC地址'),
                value: FutureBuilder(
                  future: mac,
                  initialData: '',
                  builder: (context, AsyncSnapshot<String?> snapshot) {
                    if (snapshot.hasData &&
                        snapshot.connectionState == ConnectionState.done) {
                      String id = snapshot.data ?? '';
                      return Text(id);
                    }

                    return Text('');
                  },
                ),
              ),
              SettingsTile.navigation(
                title: Text('IP地址'),
                onPressed: (c) {
                  settingIplDialog(c);
                },
                value: FutureBuilder(
                  future: ip,
                  initialData: '',
                  builder: (context, AsyncSnapshot<String?> snapshot) {
                    if (snapshot.hasData &&
                        snapshot.connectionState == ConnectionState.done) {
                      String id = snapshot.data ?? '';
                      return Text(id);
                    }

                    return Text('');
                  },
                ),
              ),
              SettingsTile.navigation(
                title: Text('子网掩码'),
                value: FutureBuilder(
                  future: subnetMask,
                  initialData: '',
                  builder: (context, AsyncSnapshot<String?> snapshot) {
                    if (snapshot.hasData &&
                        snapshot.connectionState == ConnectionState.done) {
                      String id = snapshot.data ?? '';
                      return Text(id);
                    }

                    return Text('');
                  },
                ),
              ),
              SettingsTile.navigation(
                title: Text('网关地址'),
                value: FutureBuilder(
                  future: gateway,
                  initialData: '',
                  builder: (context, AsyncSnapshot<String?> snapshot) {
                    if (snapshot.hasData &&
                        snapshot.connectionState == ConnectionState.done) {
                      String id = snapshot.data ?? '';
                      return Text(id);
                    }

                    return Text('');
                  },
                ),
              ),
              SettingsTile.navigation(
                title: Text('Modbus地址'),
                value: Text('1'),
              ),
            ],
          ),
          SettingsSection(
            title: Text('配置'),
            tiles: <SettingsTile>[
              SettingsTile.navigation(
                leading: Icon(Icons.settings_applications_outlined),
                title: Text('逻辑配置服务'),
                onPressed: (context) {
                  Navigation.navigateTo(
                    context: context,
                    screen: LogicControlSetting(),
                  );
                },
              ),
              SettingsTile.navigation(
                leading: Icon(Icons.settings_brightness_outlined),
                title: Text('RS485配置'),
                onPressed: (context) {
                  Navigation.navigateTo(
                    context: context,
                    screen: RS485Setting(),
                  );
                },
              ),
            ],
          ),
          SettingsSection(
            title: Text('帐户'),
            tiles: <SettingsTile>[
              SettingsTile.navigation(
                title: Text('公司名称'),
                onPressed: (c) {
                  settingAppTitle(c);
                },
                value: FutureBuilder(
                  future: _prefs.then((value) => value.getString("appTitle")),
                  initialData: '',
                  builder: (context, AsyncSnapshot<String?> snapshot) {
                    if (snapshot.hasData &&
                        snapshot.connectionState == ConnectionState.done) {
                      String comName = snapshot.data ?? '';
                      return Text(comName);
                    }

                    return Text('');
                  },
                ),
              ),
              SettingsTile.navigation(
                leading: Icon(Icons.account_circle),
                title: Text('关于我们'),
              ),
              SettingsTile.navigation(
                leading: Icon(Icons.logout),
                title: Text('退出应用'),
                onPressed: (context) => closeAppUsingSystemPop(),
              ),
            ],
          ),
        ],
      ),
      floatingActionButton: FloatingActionButton(
        child: Icon(Symbols.refresh),
        tooltip: "同步",
        onPressed: () async {
          if (mounted) {
            EasyLoading.show(status: '读取中...');
            await readHoldings1().whenComplete(() {
              debugPrint("读取完成");
              EasyLoading.dismiss();
            });
          }
        },
      ),
    );
  }
}

class PlatformPickerScreen extends StatelessWidget {
  const PlatformPickerScreen({
    Key? key,
    required this.platform,
    required this.platforms,
  }) : super(key: key);

  final DevicePlatform platform;
  final Map<DevicePlatform, String> platforms;

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        title: Text('Platforms'),
      ),
      body: SettingsList(
        platform: platform,
        sections: [
          SettingsSection(
            title: Text('Select the platform you want'),
            tiles: platforms.keys.map((e) {
              final platform = platforms[e];

              return SettingsTile(
                title: Text(platform!),
                onPressed: (_) {
                  Navigator.of(context).pop(e);
                },
              );
            }).toList(),
          ),
        ],
      ),
    );
  }
}
